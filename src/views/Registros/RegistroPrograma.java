    private void guardarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBotonActionPerformed
        // TODO add your handling code here:
        Programa prog = new Programa();

        prog.setCodprogr(Short.parseShort(codigo.getText()));
        prog.setDetprogr(des.getText());

        prog.setCodsector((Sector) jpam.objectListResultNQ(Sector.FIND_BY_CODE,
                new HashMap<String, Object>() {
                    {
                        put("codsector", Integer.parseInt(sectorList.getSelectedItem().toString()));
                    }
                }).get(0));

        if (jpam.persistEntity(prog)) {
            tc.updateTable(General.GeneralTable, "programa");
        }

    }//GEN-LAST:event_guardarBotonActionPerformed

