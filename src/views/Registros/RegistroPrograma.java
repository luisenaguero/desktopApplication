    private void revisarCompletitud() {

        guardarBoton.setEnabled(true);

        if (sectorList.getSelectedItem().toString().equals(" ")) {
            errorLabel1.setText("Seleccione el código del sector");
            guardarBoton.setEnabled(false);
        } else {
            errorLabel1.setText("");
        }

        if (!codigo.getText().contains(" ")) {

            // REVISO QUE EL CÓDIGO NO EXISTA
            validCode = !(jpam.codeExists(Programa.CODE_EXISTS,
                    new HashMap<String, Object>() {
                        {
                            put("codprog", Short.parseShort(codigo.getText()));
                        }
                    }));

            if (!validCode) {
                errorLabel.setText("El código ya existe");
                guardarBoton.setEnabled(false);

            } else {
                errorLabel.setText("");
            }
        } else {
            errorLabel.setText("Ingrese un código");
            guardarBoton.setEnabled(false);
        }

    }

