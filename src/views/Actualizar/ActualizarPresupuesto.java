        Presupue pre = new Presupue();
        pre.setCodpresup(Integer.parseInt(codigo.getText()));
        pre.setDescri(des.getText());
        pre.setId((Long) General.GeneralTable.getValueAt(row, 0));

        /*  pre.setCorrsoli(Double.parseDouble(corr.getText()));
         pre.setCorrpago(Double.parseDouble(corrpago.getText()));
         pre.setEmprcons(emp.getText());*/
        boolean res = jpam.updateEntity(pre);
        if (res != false) {
            tc.updateTable(General.GeneralTable, "presupuesto");

            General.PanelRegistro.removeAll();
            RegistroPresupuesto rs = new RegistroPresupuesto();
            General.PanelRegistro.setBorder(BorderFactory.createTitledBorder("Nuevo Registro"));
            General.PanelRegistro.add(rs);
            General.PanelRegistro.updateUI();

        }

