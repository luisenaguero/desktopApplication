    private void codigoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codigoKeyReleased

        if (!codigo.getText().contains(" ")) {

            // REVISO QUE EL C√ìDIGO NO EXISTA
            validCode = !(jpam.codeExists(Presupue.CODE_EXISTS,
                    new HashMap<String, Object>() {
                        {
                            put("codpresup", Short.parseShort(codigo.getText()));
                        }
                    }));

            if (!validCode) {
                errorLabel.setText("El c√≥digo ya existe");
                actualizarBoton.setEnabled(false);

            } else {
                errorLabel.setText("");
                actualizarBoton.setEnabled(true);
            }
        } else {
            errorLabel.setText("Ingrese un c√≥digo");
            actualizarBoton.setEnabled(false);
        }

// TODO add your handling code here:
    }//GEN-LAST:event_codigoKeyReleased


